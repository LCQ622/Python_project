#绘制米字格
import turtle
turtle.color("red")
turtle.showturtle()
turtle.color("blue")
turtle.forward(200)
turtle.left(90)
turtle.color("red")
turtle.forward(200)
turtle.left(90)
turtle.color("yellow")
turtle.forward(200)
turtle.left(90)
turtle.color("green")
turtle.forward(200)
turtle.color("red")
turtle.goto(200,200)
turtle.penup()
turtle.goto(0,200)
turtle.color("green")
turtle.pendown()
turtle.goto(200,0)
turtle.penup()
turtle.goto(200,100)
turtle.color("yellow")
turtle.pendown()
turtle.goto(0,100)
turtle.penup()
turtle.goto(100,0)
turtle.pendown()
